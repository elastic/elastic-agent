package ess

import (
	"context"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"testing"
	"time"

	"github.com/stretchr/testify/require"
)

func makeTestFS(t *testing.T, size uint64) string {
	t.Helper()

	tempDir := t.TempDir()
	imagePath := filepath.Join(tempDir, "disk.dmg")
	mountPoint := filepath.Join(tempDir, "mount")
	require.NoError(t, os.Mkdir(mountPoint, 0o755))
	runHdiutil(t, t.Context(),
		"create",
		"-size", fmt.Sprintf("%dm", size/(1024*1024)),
		"-fs", "HFS+",
		"-volname", "elastic-agent-diskspace-test",
		imagePath,
	)
	runHdiutil(t, t.Context(),
		"attach",
		"-nobrowse",
		"-mountpoint", mountPoint,
		imagePath,
	)
	t.Cleanup(func() {
		runHdiutil(t, context.WithoutCancel(t.Context()), "detach", "-force", mountPoint)
	})
	return mountPoint
}

func runHdiutil(t *testing.T, ctx context.Context, args ...string) {
	t.Helper()

	commandContext, cancel := context.WithTimeout(ctx, time.Minute)
	defer cancel()
	command := exec.CommandContext(commandContext, "/usr/bin/hdiutil", args...) // #nosec G204 -- all arguments are fixed or generated by this test
	output, err := command.CombinedOutput()
	require.NoError(t, err, "hdiutil failed: %s", output)
}
